export type ZodiacId =
  | "rat" | "ox" | "tiger" | "rabbit" | "dragon" | "snake"
  | "horse" | "goat" | "monkey" | "rooster" | "dog" | "pig";

export type ZodiacFortunePools = {
  general: string[];
  love: string[];
  work: string[];
  money: string[];
  health: string[];
};

export type ZodiacAnimal = {
  id: ZodiacId;
  name: string;
  emoji: string;
  trait: string;
  years: number[];
  color: string;  // tailwind text color
  fortunes: ZodiacFortunePools;
};

export type DailyFortune = {
  general: string;
  love: string;
  work: string;
  money: string;
  health: string;
  scores: { general: number; love: number; work: number; money: number; health: number };
};

// djb2 hash — stable, no floating point
function seedHash(str: string): number {
  let h = 5381;
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) + h) ^ str.charCodeAt(i);
    h = h >>> 0; // keep unsigned 32-bit
  }
  return h;
}

export function getDailyFortune(id: ZodiacId, dateStr: string): DailyFortune {
  const z = ZODIACS.find((a) => a.id === id)!;
  const seed = seedHash(`${dateStr}:${id}`);
  const pick = (arr: string[], off: number) => arr[(seed + off) % arr.length];
  const score = (off: number): number => ((seed >>> off) % 5) + 1;
  return {
    general: pick(z.fortunes.general, 0),
    love: pick(z.fortunes.love, 1),
    work: pick(z.fortunes.work, 3),
    money: pick(z.fortunes.money, 7),
    health: pick(z.fortunes.health, 13),
    scores: {
      general: score(2),
      love: score(5),
      work: score(9),
      money: score(14),
      health: score(19),
    },
  };
}

