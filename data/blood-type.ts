import { seedHash, pickFortune, type FortunePools, type FortuneResult } from "@/lib/seed";

export type BloodTypeId = "A" | "B" | "O" | "AB";

export type BloodTypeData = {
  id: BloodTypeId;
  emoji: string;
  trait: string;
  fortunes: FortunePools;
};

export function getBloodTypeFortune(id: BloodTypeId, dateStr: string): FortuneResult {
  const bt = BLOOD_TYPES.find((b) => b.id === id)!;
  return pickFortune(bt.fortunes, seedHash(`${dateStr}:blood:${id}`));
}

