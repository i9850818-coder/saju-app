export type CompatResult = {
  general: string;
  love: string;
  trust: string;
  communication: string;
  future: string;
  oneliner: string;
  scores: {
    love: number;
    trust: number;
    communication: number;
    future: number;
    total: number;
  };
};

export function calcCompat(
  p1: { birth: string; siju: string },
  p2: { birth: string; siju: string }
): CompatResult {
  const key = [
    `${p1.birth}:${p1.siju}`,
    `${p2.birth}:${p2.siju}`,
  ].sort().join("|");
  const seed = seedHash(`compat:${key}`);

  const s = (off: number) => ((seed >>> off) % 5) + 1;
  const p = (arr: string[], off: number) => arr[(seed + off) % arr.length];

  const scores = {
    love: s(0),
    trust: s(5),
    communication: s(10),
    future: s(15),
  };
  const total = Math.round(((scores.love + scores.trust + scores.communication + scores.future) / 20) * 100);

  return {
    love: p(POOLS.love, 0),
    trust: p(POOLS.trust, 3),
    communication: p(POOLS.communication, 7),
    future: p(POOLS.future, 13),
    general: p(POOLS.general, 19),
    oneliner: p(POOLS.oneliner, 23),
    scores: { ...scores, total },
  };
}
